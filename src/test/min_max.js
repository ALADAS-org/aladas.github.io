"use strict";

let p0 = { x: -0.3, y: 0.5, z: 0.7 };
let minimas = [p0.x, p0.y, p0.z].sort( (a,b) => a-b );
let maximas = [p0.x, p0.y, p0.z].sort( (a,b) => b-a );

console.log( "minimas: " + JSON.stringify(minimas) );
console.log( "maximas: " + JSON.stringify(maximas) );